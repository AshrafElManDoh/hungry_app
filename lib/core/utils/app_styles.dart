import 'package:flutter/material.dart';

abstract class AppStyles {
  static TextStyle style14() =>
      TextStyle(fontSize: 14, color: Colors.black, fontWeight: FontWeight.w400);
  static TextStyle style16() =>
      TextStyle(fontSize: 16, color: Colors.black, fontWeight: FontWeight.w700);
  static TextStyle style18() => TextStyle(
    fontSize: 18,
    color: Color(0xff7D7D7D),
    fontWeight: FontWeight.w500,
  );

  static TextStyle style32() =>
      TextStyle(fontSize: 32, color: Colors.black, fontWeight: FontWeight.w400);
}
